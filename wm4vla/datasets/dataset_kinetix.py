"""Dataset for Kinetix pixel-observation trajectories (skip-dynamics WM).

Data format (generated by real-time-chunking-kinetix/src/generate_pixels_dataset.py):
  <data_pixels_dir>/trajectories/<task_name>/chunk_XXXXXX.npz

Each .npz contains:
  observation_img: uint8  [T, E, H, W, 3]
  action:          float32 [T, E, 6]   -- already in [-1, 1]
  done:            bool   [T, E]

Skip-dynamics sampling (max_delay=5, d ∈ [0, 4]):
  For a random start t and delay d:
    - Conditioning frame : o_t (repeated ×4)
    - Predicted target   : o_{t+d+1} (repeated ×4)
    - Action input       : [a_{t+d} ; d / (max_delay-1)]  shape [1, 7]

  Video layout (9 pixel frames, state_t=3, num_conditional_frames=2):
    Frame 0    : zeros            (latent 0 – blank anchor)
    Frames 1–4 : o_t ×4          (latent 1 – conditioning)
    Frames 5–8 : o_{t+d+1} ×4   (latent 2 – predicted)

  Max raw window needed = max_delay + 1 = 6 (only frames t and t+d+1 required).

  At eval with inference_delay=D:
    Pass (o_t, a_{t+D}, D) → decode predicted latent → take frame 5.

  t5_text_embeddings: always zeros(512, 1024) — Kinetix has no task description;
    conditioner requires this key; text CFG guidance contribution is zero.
"""

import pathlib
import traceback
import warnings
from typing import Sequence

import numpy as np
import torch
import torchvision.transforms.functional as TF
from torch.utils.data import Dataset

# Canonical task list matching real-time-chunking-kinetix/src/train_world_model.py
KINETIX_TASK_NAMES: tuple[str, ...] = (
    "worlds_l_car_launch",
    "worlds_l_cartpole_thrust",
    "worlds_l_catapult",
    "worlds_l_catcher_v3",
    "worlds_l_chain_lander",
    "worlds_l_grasp_easy",
    "worlds_l_h17_unicycle",
    "worlds_l_hard_lunar_lander",
    "worlds_l_mjc_half_cheetah",
    "worlds_l_mjc_swimmer",
    "worlds_l_mjc_walker",
    "worlds_l_trampoline",
)

_MAX_DONE_RETRIES = 16

# state_t=3: 9 pixel frames total (1 blank + 4 cond + 4 pred).
# VAE temporal compression = 4: each non-first latent encodes 4 pixel frames.
_NUM_PRED_FRAMES = 4


class KinetixPixelDataset(Dataset):
    """PyTorch Dataset for Kinetix pixel trajectory chunks (skip-dynamics).

    Each sample is a skip-dynamics window with a randomly drawn delay d:
      video  : [3, 5, H, W] uint8  — [o_t, o_{t+d+1}×4]  (state_t=2)
      action : [1, 7] float32      — [a_{t+d} ; d/(max_delay-1)]

    Returns dicts compatible with the cosmos action-conditioned training interface:
      video         : uint8 Tensor  [3, 5, H, W]
      action        : float32 Tensor [1, 7]   (6-dim action + normalised delay)
      __key__       : str   unique sample identifier
      fps           : int   (fixed 4)
      image_size    : Tensor [4]  (fixed 256, legacy interface)
      num_frames    : int   (= 5)
      padding_mask  : Tensor [1, 256, 256]  (zeros, legacy interface)
      ai_caption    : str   task name in human-readable form
    """

    def __init__(
        self,
        data_pixels_dir: str,
        task_names: Sequence[str] = KINETIX_TASK_NAMES,
        max_delay: int = 5,
        val_ratio: float = 0.1,
        mode: str = "train",
        seed: int = 0,
        video_size: list = None,
    ):
        """
        Args:
            data_pixels_dir: Path to the <run_path>/data_pixels directory.
            task_names: Which Kinetix tasks to include.
            max_delay: Maximum async delay D_max; delay d is sampled from [0, max_delay-1].
                       Requires episode length ≥ max_delay + 1.
            val_ratio: Fraction of chunks held out for validation.
            mode: "train" or "val".
            seed: RNG seed for the train/val split.
            video_size: [H, W] to resize frames to (None = keep native 128×128).
        """
        super().__init__()
        if mode not in ("train", "val"):
            raise ValueError(f"mode must be 'train' or 'val', got {mode!r}")

        self.data_pixels_dir = pathlib.Path(data_pixels_dir)
        self.max_delay = max_delay
        # Only need frames at t and t+d+1; max d = max_delay-1
        self._max_window = max_delay + 1                       # = 6 for max_delay=5
        self.sequence_length = 1 + 2 * _NUM_PRED_FRAMES       # = 9 (blank + cond×4 + pred×4)
        self.mode = mode
        self.video_size = video_size

        # ── collect all chunk paths ───────────────────────────────────────────
        all_chunks: list[pathlib.Path] = []
        for task_name in task_names:
            task_dir = self.data_pixels_dir / "trajectories" / task_name
            if not task_dir.exists():
                warnings.warn(f"[KinetixPixelDataset] task directory not found: {task_dir}")
                continue
            all_chunks.extend(sorted(task_dir.glob("chunk_*.npz")))

        if not all_chunks:
            raise FileNotFoundError(f"No chunk files found under {self.data_pixels_dir}/trajectories/")

        # ── train/val split at chunk level ───────────────────────────────────
        rng = np.random.default_rng(seed)
        perm = rng.permutation(len(all_chunks))
        n_val = max(1, int(len(all_chunks) * val_ratio))
        val_mask = set(perm[:n_val].tolist())

        self.chunk_paths: list[pathlib.Path] = [
            all_chunks[i] for i in range(len(all_chunks)) if (i in val_mask) == (mode == "val")
        ]

        # ── verify chunk shape using the first available chunk ────────────────
        with np.load(self.chunk_paths[0]) as d:
            self._T: int = int(d["observation_img"].shape[0])
            self._E: int = int(d["observation_img"].shape[1])
            self._H: int = int(d["observation_img"].shape[2])
            self._W: int = int(d["observation_img"].shape[3])

        # valid start positions: need start_t + max_delay ≤ T - 1
        # i.e., start_t ≤ T - max_window  (max_window = max_delay + 1)
        self._valid_starts: int = self._T - self._max_window + 1
        if self._valid_starts <= 0:
            raise ValueError(
                f"Chunk length T={self._T} is too short for max_window={self._max_window} "
                f"(max_delay={max_delay}). Reduce max_delay or increase T."
            )

        self._samples_per_chunk: int = self._valid_starts * self._E

        print(
            f"[KinetixPixelDataset] mode={mode}: {len(self.chunk_paths)} chunks, "
            f"~{len(self):,} samples "
            f"(T={self._T}, E={self._E}, max_delay={max_delay}, "
            f"valid_starts/env={self._valid_starts})"
        )

    # ── index arithmetic ──────────────────────────────────────────────────────

    def __len__(self) -> int:
        return len(self.chunk_paths) * self._samples_per_chunk

    def _flat_to_coords(self, index: int) -> tuple[int, int, int]:
        """Convert flat index to (chunk_idx, env_idx, start_t)."""
        chunk_idx = index // self._samples_per_chunk
        local = index % self._samples_per_chunk
        env_idx = local // self._valid_starts
        start_t = local % self._valid_starts
        return chunk_idx, env_idx, start_t

    # ── data loading ─────────────────────────────────────────────────────────

    def __getitem__(self, index: int) -> dict:
        for attempt in range(_MAX_DONE_RETRIES):
            try:
                return self._load_sample(index)
            except _DoneBoundaryError:
                index = int(np.random.randint(len(self)))
            except Exception:
                warnings.warn(f"[KinetixPixelDataset] error loading index={index} (attempt {attempt})")
                warnings.warn(traceback.format_exc())
                index = int(np.random.randint(len(self)))

        warnings.warn("[KinetixPixelDataset] exceeded max retries; returning last attempted sample")
        return self._load_sample(index)

    def _load_sample(self, index: int) -> dict:
        chunk_idx, env_idx, start_t = self._flat_to_coords(index)
        chunk_path = self.chunk_paths[chunk_idx]

        # Sample delay d ∈ [0, max_delay-1]
        d = int(np.random.randint(0, self.max_delay))

        with np.load(chunk_path) as data:
            # Check for episode boundaries between t and t+d (i.e. done[t..t+d-1])
            done_window = data["done"][start_t : start_t + d + 1, env_idx]
            if done_window.any():
                raise _DoneBoundaryError()

            # Load only 2 frames: o_t (conditioning) and o_{t+d+1} (prediction target)
            cond_img = data["observation_img"][start_t, env_idx]          # [H, W, 3]
            pred_img  = data["observation_img"][start_t + d + 1, env_idx] # [H, W, 3]

            # Load the single skip action: a_{t+d}
            act = data["action"][start_t + d, env_idx]  # [6] float32

        H, W = cond_img.shape[:2]
        blank = np.zeros((H, W, 3), dtype=np.uint8)

        # Build 9-frame video (state_t=3, num_conditional_frames=2):
        # Frame 0    : zeros       (latent 0 – blank anchor)
        # Frames 1–4 : o_t ×4     (latent 1 – conditioning)
        # Frames 5–8 : pred ×4    (latent 2 – predicted)
        imgs = np.stack(
            [blank,
             cond_img, cond_img, cond_img, cond_img,
             pred_img, pred_img, pred_img, pred_img],
            axis=0,
        )  # [9, H, W, 3]

        # ── images → [3, 9, H, W] uint8 ─────────────────────────────────────
        video = torch.from_numpy(imgs).permute(3, 0, 1, 2)  # [3, 9, H, W]

        if self.video_size is not None:
            tgt_h, tgt_w = self.video_size
            if video.shape[2] != tgt_h or video.shape[3] != tgt_w:
                video = video.permute(1, 0, 2, 3)  # [9, 3, H, W]
                video = torch.stack(
                    [TF.resize(video[t], [tgt_h, tgt_w], antialias=True) for t in range(video.shape[0])]
                )
                video = video.permute(1, 0, 2, 3)  # [3, 9, H, W]

        # ── action with normalised delay → [1, 7] float32 ────────────────────
        # Normalise d to [0, 1]; max_delay-1 is the largest possible d.
        d_norm = float(d) / float(max(self.max_delay - 1, 1))
        action_vec = np.concatenate([act.astype(np.float32), np.array([d_norm], dtype=np.float32)])
        action = torch.from_numpy(action_vec).unsqueeze(0)  # [1, 7]

        # ── metadata ─────────────────────────────────────────────────────────
        task_name = chunk_path.parent.name
        key = f"{task_name}/{chunk_path.stem}_e{env_idx:03d}_t{start_t:02d}_d{d}"
        caption = task_name.removeprefix("worlds_l_").replace("_", " ")

        return {
            "video": video,                                          # [3, 9, H, W] uint8
            "action": action,                                        # [1, 7] float32
            "__key__": key,
            "fps": 4,
            "image_size": 256 * torch.ones(4).cuda(),
            "num_frames": self.sequence_length,
            "padding_mask": torch.zeros(1, 256, 256).cuda(),
            # Kinetix has no task description; conditioner requires this key.
            # Text CFG guidance contribution is zero (always zero embedding).
            "t5_text_embeddings": torch.zeros(512, 1024, dtype=torch.float32),
        }


class _DoneBoundaryError(Exception):
    """Raised when a sampled window crosses an episode boundary."""
